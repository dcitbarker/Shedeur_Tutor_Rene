public class Maths {
    double a, b;
    double result;


    public double Add(){
        result = a + b;
        return result;
    }
    public void divide(){
        result = a / b;
    }
    public void multiply(){
        result = a * b;
    }
    public void subtract(){
        result = a - b;
    }
    public double getResult(){
        return result;
    }


}
